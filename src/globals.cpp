namespace sim
{

bool
QUBIT::operator==(const QUBIT& other) const
{
    return (qubit_id == other.qubit_id) && (client_id == other.client_id);
}

std::string
QUBIT::to_string() const
{
    std::stringstream ss;
    ss << *this;
    return ss.str();
}

std::ostream&
operator<<(std::ostream& out, const QUBIT& q)
{
    out << q.qubit_id << "(" << q.client_id << ")";
    return out;
}

} // namespace sim

